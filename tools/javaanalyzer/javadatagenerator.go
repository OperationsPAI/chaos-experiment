func discoverServicePaths(basePath string) ([]string, error) {
	buildFiles := map[string]struct{}{
		"pom.xml":          {},
		"build.gradle":     {},
		"build.gradle.kts": {},
	}

	candidates := make(map[string]struct{})
	err := filepath.WalkDir(basePath, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return nil
		}

		if d.IsDir() {
			name := d.Name()
			if name == ".git" || name == "node_modules" || name == "vendor" || name == "build" || name == "target" {
				return filepath.SkipDir
			}
			return nil
		}

		if _, ok := buildFiles[d.Name()]; ok {
			candidates[filepath.Dir(path)] = struct{}{}
		}

		return nil
	})
	if err != nil {
		return nil, err
	}

	servicePaths := make([]string, 0, len(candidates))
	for moduleDir := range candidates {
		if hasJavaSources(moduleDir) {
			servicePaths = append(servicePaths, moduleDir)
		}
	}

	sort.Strings(servicePaths)

	leafPaths := make([]string, 0, len(servicePaths))
	for i, p := range servicePaths {
		isParent := false
		prefix := p + string(os.PathSeparator)
		for j, q := range servicePaths {
			if i == j {
				continue
			}
			if strings.HasPrefix(q, prefix) {
				isParent = true
				break
			}
		}
		if !isParent {
			leafPaths = append(leafPaths, p)
		}
	}

	return leafPaths, nil
}

func hasJavaSources(moduleDir string) bool {
	mainJavaDir := filepath.Join(moduleDir, "src", "main", "java")
	if info, err := os.Stat(mainJavaDir); err == nil && info.IsDir() {
		return true
	}

	hasJava := false
	_ = filepath.WalkDir(moduleDir, func(path string, d os.DirEntry, err error) error {
		if err != nil || hasJava {
			return nil
		}

		if d.IsDir() {
			name := d.Name()
			if name == ".git" || name == "node_modules" || name == "vendor" || name == "build" || name == "target" {
				return filepath.SkipDir
			}
			return nil
		}

		if strings.HasSuffix(d.Name(), ".java") {
			hasJava = true
		}

		return nil
	})

	return hasJava
}

func filterServicePathsByNetworkServices(servicePaths []string) []string {
	networkServices := serviceendpoints.GetAllServices()
	if len(networkServices) == 0 {
		return servicePaths
	}

	allowed := make(map[string]struct{}, len(networkServices))
	for _, serviceName := range networkServices {
		allowed[serviceName] = struct{}{}
	}

	filtered := make([]string, 0, len(servicePaths))
	for _, servicePath := range servicePaths {
		serviceName := normalizeServiceName(filepath.Base(servicePath))
		if _, ok := allowed[serviceName]; ok {
			filtered = append(filtered, servicePath)
		}
	}

	return filtered
}

func normalizeServiceName(rawName string) string {
	if rawName == "" {
		return rawName
	}

	if systemconfig.IsTeaStore() {
		const teaStoreModulePrefix = "tools.descartes.teastore."
		if strings.HasPrefix(rawName, teaStoreModulePrefix) {
			suffix := strings.TrimPrefix(rawName, teaStoreModulePrefix)
			if suffix != "" {
				return "teastore-" + suffix
			}
		}
	}

	return rawName
}

// getPackageNameFromPath extracts the package name from the output file path
func getPackageNameFromPath(outputFilePath string) string {
	dir := filepath.Dir(outputFilePath)
	packageName := filepath.Base(dir)
	// Sanitize package name (replace - with _ for valid Go package names)
	packageName = strings.ReplaceAll(packageName, "-", "_")
	return packageName
}

// FilterClassMethods filters out unwanted class-method entries based on system-specific rules
func FilterClassMethods(entries []ClassMethodEntry) []ClassMethodEntry {
	var filtered []ClassMethodEntry
	for _, entry := range entries {
		// For OTel Demo, exclude problempattern classes
		if systemconfig.IsOtelDemo() {
			if strings.Contains(entry.ClassName, "problempattern") {
				continue
			}
		}
		// For TeaStore, exclude test classes
		if systemconfig.IsTeaStore() {
			if strings.Contains(entry.ClassName, "Test") || strings.Contains(entry.ClassName, "test") {
				continue
			}
		}
		filtered = append(filtered, entry)
	}
	return filtered
}

// GenerateJavaClassMethodsFile analyzes Java services and generates a Go file
// with class-method pairs for each service
func GenerateJavaClassMethodsFile(servicesBasePath string, outputFilePath string) error {
	var (
		basePath     = servicesBasePath
		servicePaths []string
		err          error
	)

	if systemconfig.IsTeaStore() {
		basePath = filepath.Join(servicesBasePath, "services")
		servicePaths = make([]string, 0, len(teaStoreServiceDirs))
		for _, serviceDir := range teaStoreServiceDirs {
			servicePath := filepath.Join(basePath, serviceDir)
			if info, statErr := os.Stat(servicePath); statErr == nil && info.IsDir() {
				servicePaths = append(servicePaths, servicePath)
			}
		}
	} else if systemconfig.IsOtelDemo() {
		basePath = filepath.Join(servicesBasePath, "src")
		if info, statErr := os.Stat(basePath); statErr != nil || !info.IsDir() {
			return fmt.Errorf("oteldemo services root not found: %s", basePath)
		}
		servicePaths, err = discoverServicePaths(basePath)
		if err != nil {
			return fmt.Errorf("failed to discover oteldemo service directories: %w", err)
		}
	} else {
		servicePaths, err = discoverServicePaths(basePath)
		if err != nil {
			return fmt.Errorf("failed to discover service directories: %w", err)
		}
	}

	for _, servicePath := range servicePaths {
		fmt.Printf("Found service directory: %s\n", servicePath)
	}

	servicePaths = filterServicePathsByNetworkServices(servicePaths)
	for _, servicePath := range servicePaths {
		fmt.Printf("Selected service directory: %s\n", servicePath)
	}

	if len(servicePaths) == 0 {
		fmt.Printf("Warning: No service directories found in %s\n", basePath)
	}

	// Analyze Java services
	pathResults, err := AnalyzeJavaPaths(servicePaths)
	if err != nil {
		return fmt.Errorf("failed to analyze Java services: %w", err)
	}

	fmt.Printf("Analyzed %d service paths\n", len(pathResults))

	// Transform path results to service class methods
	var services []ServiceClassMethods
	for _, result := range pathResults {
		serviceName := normalizeServiceName(result.PathName)

		// Filter out unwanted classes
		filteredMethods := FilterClassMethods(result.Methods)

		fmt.Printf("Service %s: found %d methods (after filtering)\n", serviceName, len(filteredMethods))

		services = append(services, ServiceClassMethods{
			ServiceName: serviceName,
			Methods:     filteredMethods,
		})
	}

	// Get package name from output path
	packageName := getPackageNameFromPath(outputFilePath)

	// Create template data
	templateData := struct {
		PackageName string
		SystemType  string
		Services    []ServiceClassMethods
	}{
		PackageName: packageName,
		SystemType:  string(systemconfig.GetCurrentSystem()),
		Services:    services,
	}

	// Ensure output directory exists
	outputDir := filepath.Dir(outputFilePath)
	if err := os.MkdirAll(outputDir, 0755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Parse template
	tmpl, err := template.New("javaMethodsTemplate").Parse(javaMethodsTemplate)
	if err != nil {
		return fmt.Errorf("failed to parse template: %w", err)
	}

	// Create output file
	outFile, err := os.Create(outputFilePath)
	if err != nil {
		return fmt.Errorf("failed to create output file: %w", err)
	}
	defer outFile.Close()

	// Execute template
	if err := tmpl.Execute(outFile, templateData); err != nil {
		return fmt.Errorf("failed to execute template: %w", err)
	}

	// Also generate runtime mutator config file in internal/<system>/mutatorconfig/mutatorconfig.go
	mutatorOutputFilePath := filepath.Join(filepath.Dir(filepath.Dir(outputFilePath)), "mutatorconfig", "mutatorconfig.go")
	if err := GenerateMutatorConfigFile(services, mutatorOutputFilePath); err != nil {
		return fmt.Errorf("failed to generate mutator config file: %w", err)
	}

	return nil
}

// GenerateMutatorConfigFile generates a Go file with runtime mutator configs.
func GenerateMutatorConfigFile(services []ServiceClassMethods, outputFilePath string) error {
	mutatorServices := make([]ServiceMutatorConfig, 0, len(services))
	for _, service := range services {
		entries := make([]MethodMutationEntry, 0, len(service.Methods))
		for _, method := range service.Methods {
			// Keep only methods with real mutation data from extractor.
			if len(method.Mutations) == 0 {
				continue
			}

			entries = append(entries, MethodMutationEntry{
				ClassName:  method.ClassName,
				MethodName: method.MethodName,
				Mutations:  method.Mutations,
			})
		}

		// Skip services that have no methods with real mutations.
		if len(entries) == 0 {
			continue
		}

		mutatorServices = append(mutatorServices, ServiceMutatorConfig{
			ServiceName: service.ServiceName,
			Entries:     entries,
		})
	}

	outputDir := filepath.Dir(outputFilePath)
	if err := os.MkdirAll(outputDir, 0755); err != nil {
		return fmt.Errorf("failed to create mutator output directory: %w", err)
	}

	packageName := getPackageNameFromPath(outputFilePath)
	templateData := struct {
		PackageName string
		SystemType  string
		Services    []ServiceMutatorConfig
	}{
		PackageName: packageName,
		SystemType:  string(systemconfig.GetCurrentSystem()),
		Services:    mutatorServices,
	}

	tmpl, err := template.New("mutatorConfigTemplate").Parse(mutatorConfigTemplate)
	if err != nil {
		return fmt.Errorf("failed to parse mutator config template: %w", err)
	}

	outFile, err := os.Create(outputFilePath)
	if err != nil {
		return fmt.Errorf("failed to create mutator output file: %w", err)
	}
	defer outFile.Close()

	if err := tmpl.Execute(outFile, templateData); err != nil {
		return fmt.Errorf("failed to execute mutator config template: %w", err)
	}

	return nil
}
